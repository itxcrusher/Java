package Fundamentals;

import java.util.ArrayList;
import java.util.List;

// ! Data types in Java

public class DataTypes {
	// * Primitive Datatypes
	int myIntNum = 5;            // ? Integer (whole number)
	float myFloatNum = 5.99f;    // ? Floating point number
	char myLetter = 'D';         // ? Character
	boolean myBool = true;       // ? Boolean

	// * Reference Datatypes
	String stringValue = "Hello";       // ? String (class)
	Object objectValue = new Object();  // ? Object (class)
	int[] arrayValue = {1, 2, 3};       // ? Array (Static Size)
	List<String> listValue = new ArrayList<>(); // ? List (Dynamic Size)
}